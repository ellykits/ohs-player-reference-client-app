package dev.ohs.player.library.extractor

import dev.ohs.fhir.fhirpath.FhirPathEngine
import dev.ohs.fhir.model.r4.Resource
import dev.ohs.player.library.model.SearchResult

/**
 * Contract for generated state extractors.
 *
 * Each concrete subclass is code-generated by the ig-codegen plugin from a ViewJoinMap Binary JSON
 * file. It knows exactly which resources to pull from a [SearchResult], which FHIRPath expressions
 * to evaluate, and how to populate the corresponding state class [T].
 *
 * Usage:
 * ```kotlin
 * val states: List<PatientAllergyState> =
 *     PatientAllergyExtractor.extract(fhirPathEngine, searchResult)
 * ```
 */
abstract class StateExtractor<T : Any> {

  /** Extracts a list of [T] from [result] by evaluating FHIRPath expressions via [engine]. */
  abstract suspend fun extract(engine: FhirPathEngine, result: SearchResult<Resource>): List<T>
}
